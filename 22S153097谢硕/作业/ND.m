x=[];
x(1,:) = [0,0];
f = [];
for i = 1: 1: 50
    f(i) = exp(x(i,1)+3*x(i,2)-0.1)+exp(x(i,1)-3*x(i,2)-0.1)+exp(-x(i,1)-0.1);
    df1 = exp(x(i,1)+3*x(i,2)-0.1)+exp(x(i,1)-3*x(i,2)-0.1)-exp(-x(i,1)-0.1);
    df2 = 3*exp(x(i,1)+3*x(i,2)-0.1)-3*exp(x(i,1)-3*x(i,2)-0.1);
    ddf = [exp(x(i,1)+3*x(i,2)-0.1)+exp(x(i,1)-3*x(i,2)-0.1)+exp(-x(i,1)-0.1),3*exp(x(i,1)+3*x(i,2)-0.1)-3*exp(x(i,1)-3*x(i,2)-0.1);
        3*exp(x(i,1)+3*x(i,2)-0.1)-3*exp(x(i,1)-3*x(i,2)-0.1),9*exp(x(i,1)+3*x(i,2)-0.1)+9*exp(x(i,1)-3*x(i,2)-0.1)];
    x(i+1,:) = ((x(i,:))'-(ddf)^(-1)*[df1;df2])'; 
    plot(i,f(i),'*');
    hold on;
end